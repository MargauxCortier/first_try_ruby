# #{} sert a evaluer l'expression qui se trouve en tre les deux {} le resultat est ensuite affiché sur notre console. 

#nous posons une question
puts "On va compter le nombre d'heures de travail à THP"
#Dans un commentaire nous entrons le resultat qui va s'afficher du calcul que nous venons de poser
puts "Travail : #{10 * 5 * 11}"
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"

puts "et en secondes ?"

#nous demandons à notre reuby de faire ce calcul et d'afficher le resultat
puts 10 * 5 * 11 * 60 * 60

